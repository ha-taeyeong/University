# 8자리 이하의 문자열 하나를 입력받고, 작업 후 8자리 문자열을 출력하고자 한다.
#
# 문자열이 숫자로만 이뤄진 경우 문자열 나머지 공간은 ‘0’ 으로 채우고,
#
# 숫자이외의 값이 포함된 경우 ‘-’ 로 자리를 채우는 프로그램을 작성해보자.
#
# 조건
