# 다음 그림과 같이 3차원 np1 배열을 만들고, 각 평면에 2와 4를 더한 새로운 3차원 배열을 만드는 프로그램을 작성하시오.
# 조건
# 브로드케스팅 이용
import numpy as np  # numpy 라이브러리를 np라는 이름으로 불러오기

# 0~23까지의 숫자를 2블록(첫 번째 축), 3행, 4열로 만들어 np1에 저장
np1 = np.arange(24).reshape(2, 3, 4)

np2 = np.copy(np1)  # np1을 복사하여 np2에 저장 (깊은 복사)

np2[0] = np.add(np2[0], 2)  # 첫 번째 블록(0번 인덱스)에 2를 더한다
np2[1] = np.add(np2[1], 4)  # 두 번째 블록(1번 인덱스)에 4를 더한다

# 결과 출력
print(np1)  # 원본 배열 출력
print()
print(np2)  # 블록별로 값이 더해진 배열 출력